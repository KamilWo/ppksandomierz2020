        <h3><strong>DEKRET</strong><br />
            Zasady obowiązujące w związku z nową falą pandemii koronawirusa
        </h3>
        <p>Żródło: <a href="https://diecezjasandomierska.pl/zasady-obowiazujace-w-zwiazku-z-nowa-fala-pandemii-koronawirusa/"
        title="Dekret w związku z nową falą pandemii">link do dekretu</a></p>
    </div>
    <div class="col">
<p>Kierując się troską o&nbsp;Siostry i&nbsp;Braci narażonych
w&nbsp;czasie pandemii na&nbsp;liczne zagrożenia oraz mając
na&nbsp;względzie zbawczą misję powierzoną przez Chrystusa Kościołowi,
przypominam obowiązujące dotychczas zasady i&nbsp;stanowię dodatkowe,
które należy niezwłocznie wdrożyć podczas liturgii i&nbsp;innych
działań duszpasterskich.</p>

<p>Proszę o&nbsp;jednocześnie wszystkich Księży, aby pozostali
w&nbsp;bliskim kontakcie z&nbsp;wiernymi – z&nbsp;zachowaniem
obowiązujących przepisów sanitarnych – w&nbsp;celu zapewniania
im&nbsp;opieki duchowej i&nbsp;wychodzenia naprzeciw innym bieżącym
potrzebom. Jesteśmy wspólnotą, w&nbsp;której mamy wspierać się
nawzajem, służąc sobie z&nbsp;miłością.</p>

<h4>1. Liczba uczestników nabożeństw liturgicznych</h4>

<p>Przypominam, że&nbsp;zgodnie z&nbsp;obowiązującymi zarządzeniami
sanitarnymi, podczas nabożeństw liturgicznych może przebywać
w&nbsp;strefie żółtej – 1&nbsp;osoba na&nbsp;4&nbsp;m² natomiast
w&nbsp;strefie czerwonej 1&nbsp;osoba na&nbsp;7&nbsp;m².</p>

<h4>2. Dyspensa</h4>

<p>Zgodnie z&nbsp;kan.&nbsp;87 §&nbsp;1 Kodeksu Prawa Kanonicznego,
udzielam do&nbsp;odwołania dyspensy od&nbsp;obowiązku niedzielnego
uczestnictwa we&nbsp;Mszy Świętej następującym wiernym:</p>
<ul>
    <li>– osobom z&nbsp;objawami infekcji,</li>
    <li>– osobom w&nbsp;podeszłym wieku,</li>
    <li>– kobietom w&nbsp;stanie błogosławionym,</li>
    <li>– osobom, które czują wielką obawę przed zarażeniem.</li>
</ul>

<p>W&nbsp;związku z&nbsp;ograniczeniami dotyczącymi liczby
uczestników zgromadzeń religijnych w&nbsp;tzw. „czerwonej strefie”,
udzielam ponadto ogólnej dyspensy od&nbsp;wspomnianego obowiązku
wszystkim osobom przebywającym we&nbsp;wspomnianej strefie podczas
jej obowiązywania.</p>

<h4>3. Środki ochrony osobistej</h4>

<p>Maseczek lub przyłbic zakrywających nos i&nbsp;usta winni używać:</p>
<ul>
    <li>– wierni uczestniczący w&nbsp;liturgii, chyba że&nbsp;mają
    zwolnienie lekarskie z&nbsp;tego obowiązku zgodnie
    z&nbsp;przepisami sanitarnymi,</li>
    <li>– pracownicy kościelni będący w&nbsp;kontakcie z&nbsp;innymi
    osobami, np.&nbsp;podczas zbierania składki czy w&nbsp;czasie
    przygotowania paramentów w&nbsp;zakrystii oraz w&nbsp;świątyni,</li>
    <li>– Służba Liturgiczna podczas liturgii i&nbsp;przygotowania
    do&nbsp;niej, z&nbsp;wyjątkiem diakona, lektora i&nbsp;psalmisty
    w&nbsp;momencie czytania lub śpiewu,</li>
    <li>– koncelebransi, przynajmniej podczas rozdawania Komunii św.
    i&nbsp;procesji przez świątynię.</li>
</ul>

<p>Parafia powinna zaopatrzyć się w&nbsp;dozowniki płynu
dezynfekującego, które należy ustawić przy drzwiach wejściowych
(lecz nie w&nbsp;kropielnicach). Należy ponadto umieścić przy
wszystkich drzwiach wejściowych przypomnienie o&nbsp;obowiązku
stosowania maseczek.</p>
<p>W&nbsp;przypadku gdyby ktoś nie posiadał maseczki, duszpasterze
powinni zaoferować ją&nbsp;nieodpłatnie.</p>

<h4>4. Udzielanie Komunii Świętej</h4>

<p>Podczas każdej liturgii należy umożliwić wiernym przyjęcie Komunii
Świętej zarówno do&nbsp;ust, jak i&nbsp;na&nbsp;rękę. Nie wolno
zmuszać nikogo do&nbsp;przyjmowania Komunii Świętej na&nbsp;rękę, jak
również nie wolno pozbawiać wiernych możliwości przyjęcia Komunii
Świętej na&nbsp;rękę, nawet jeśli wierni wprost nie domagają się
tego. Niedopuszczalne jest głoszenie w&nbsp;tej materii teorii
niezgodnych z&nbsp;obowiązującą dyscypliną Kościoła.</p>

<p>Za&nbsp;każdym razem należy wyraźnie ogłosić porządek przyjmowania
Komunii Świętej. Należy utworzyć oddzielne rzędy dla przyjmujących
Komunię Świętą do&nbsp;ust i&nbsp;na&nbsp;rękę. Jeśli obecny jest
jeden szafarz, komunikuje on&nbsp;najpierw rząd przyjmujących
na&nbsp;rękę, a&nbsp;następnie rząd przyjmujących do&nbsp;ust. Jeśli
jest większa liczba szafarzy, wówczas każdy z&nbsp;nich komunikuje
w&nbsp;oddzielnym rzędzie.</p>

<p>Kapłan lub inny szafarz rozdający Komunię Świętą winien
bezpośrednio przed tą&nbsp;czynnością zdezynfekować ręce, podczas
niej mieć nałożoną maseczkę, a&nbsp;po&nbsp;zakończeniu obmyć palce
w&nbsp;vasculum i&nbsp;powtórnie zdezynfekować ręce.</p>

<h4>5. Sakrament pokuty i&nbsp;pojednania</h4>

<p>Należy zadbać o&nbsp;należyte zabezpieczenie konfesjonałów,
co&nbsp;jest ważne zwłaszcza w&nbsp;kontekście zbliżającego się
okresu spowiedzi przed Uroczystością Wszystkich Świętych oraz przed
Bożym Narodzeniem.</p>

<p>Nie wolno używać tzw. konfesjonałów zamkniętych (szafowych).
W&nbsp;konfesjonałach, na&nbsp;kratkach od&nbsp;strony penitenta,
należy umieścić folie ochronne i&nbsp;wymieniać je&nbsp;przynajmniej
raz w&nbsp;tygodniu. Należy tam również umieścić środek
do&nbsp;dezynfekcji z&nbsp;dozownikiem rozpylającym, prosząc
penitentów, aby każdy po&nbsp;sobie zdezynfekował folię.</p>

<h4>6. Uroczystość Wszystkich Świętych i&nbsp;Dzień Zaduszny</h4>

<p>W&nbsp;związku z&nbsp;Uroczystością Wszystkich Świętych
i&nbsp;Dniem Zadusznym wskazane jest indywidualne nawiedzanie
cmentarzy, rozkładając odwiedziny grobów swoich bliskich na&nbsp;całą
oktawę Uroczystości.</p>

<p>Proszę Duszpasterzy, aby rozważyli możliwość zrezygnowania
z&nbsp;jednej uroczystej Mszy&nbsp;św. i&nbsp;procesji w&nbsp;dniach
1&nbsp;lub 2&nbsp;listopada, a&nbsp;zamiast tego zaprosili wiernych
do&nbsp;udziału w&nbsp;liturgiach sprawowanych w&nbsp;ciągu dnia
w&nbsp;świątyniach. Zamiast jednej tradycyjnej liturgii
na&nbsp;cmentarzu, można zaproponować nabożeństwa za&nbsp;zmarłych
w&nbsp;kolejnych dniach listopada, z&nbsp;zachowaniem obowiązujących
ograniczeń i&nbsp;wszelkich środków ostrożności.</p>

<h4>7. Zachęcam ponadto, aby na&nbsp;zakończenie Mszy Świętych
powrócić do&nbsp;praktyki śpiewu Suplikacji w&nbsp;intencji ustania
epidemii, za&nbsp;chorych i&nbsp;personel medyczny oraz
w&nbsp;intencji zmarłych.</h4>

<h4>Dziękując Duszpasterzom, Osobom Konsekrowanym oraz wszystkim
Siostrom i&nbsp;Braciom w&nbsp;Chrystusie za&nbsp;zrozumienie
i&nbsp;budowanie wspólnoty Kościoła, otaczam każdego modlitwą<br />
i&nbsp;z&nbsp;serca błogosławię.</h4>

