        na&nbsp;15&nbsp;m<sup>2</sup>. U&nbsp;nas w&nbsp;kościele może
        uczestniczyć 47&nbsp;osób. Zapraszamy zatem korzystajcie
        z&nbsp;tej możliwości. Od&nbsp;jutra w&nbsp;dni powszednie
        Msza Święta będzie odprawiana o&nbsp;6.30 i&nbsp;18.00.</p>
        <p>2. Przypominamy, że&nbsp;Ks.&nbsp;bp&nbsp;udzielił dyspensy
        od&nbsp;udziału we&nbsp;Mszy Świętej w&nbsp;niedziele
        do&nbsp;odwołania. Zachęca nas jednak do&nbsp;udziału we&nbsp;Mszy
        Świętej korzystając z&nbsp;mediów zarówno ogólnopolskich, jak
        i&nbsp;lokalnych.</p>
        <p>3. Kościół jest otwarty przez cały dzień, zachęcamy
        do&nbsp;modlitwy przed Najświętszym Sakramentem o&nbsp;ustanie
        epidemii.</p>
        <p>4. W&nbsp;minionym tygodniu odwołał Pan Bóg z&nbsp;naszej Parafii
        do&nbsp;wieczności: Czesławę Lipiec, Andrzeja Walicha, Teodozję
        Marczyk i&nbsp;Mieczysława Osmalę. Polećmy ich Bożemu miłosierdziu
        odmawiając: <em>Wieczny odpoczynek...</em></p>
        <p>5. Katechizacja przygotowująca do&nbsp;Sakramentu małżeństwa dla
        tych, którzy planują brać ślub do&nbsp;września jest prowadzona przez
        Internet. Katechezy są&nbsp;we&nbsp;wtorek i&nbsp;piątek
        o&nbsp;godzinie 20.00. Informacja szczegółowa dla zainteresowanych
        jest w&nbsp;gablocie.</p>
        <p>6. <em>Bóg zapłać</em> za&nbsp;prace dla dobra parafii Panom:
        Tadeuszowi i&nbsp;Ryszardowi.</p>
        <p>7. <strong>Do&nbsp;sprzątania prosimy w&nbsp;sobotę
        na&nbsp;godzinę 7.00</strong> Rodziny z&nbsp;bloku Maciejowskiego 6
        od&nbsp;46 do&nbsp;50 włącznie.</p>
        <p>8. Na&nbsp;stoliku pod chórem jest najnowsza prasa katolicka.
        Zachęcamy do&nbsp;nabywania i&nbsp;czytania prasy katolickiej.</p>
        <p>9. Trwają zapisy na&nbsp;pielgrzymkę do&nbsp;Medjugorie oraz
        innych miejsc w&nbsp;Bośni i&nbsp;Hercegowinie, Chorwacji, Węgrzech.
        Bliższe informacje na&nbsp;plakacie w&nbsp;gablocie przy wejściu
        do&nbsp;kościoła oraz w&nbsp;zakrystii.</p>
        <p>10. Wszystkim, którzy w&nbsp;tym tygodniu obchodzą swoje imieniny,
        urodziny, jakiekolwiek rocznice, gościom, uczestnikom liturgii,
        składamy najlepsze życzenia i&nbsp;zapewniamy o&nbsp;naszej modlitwie
        w&nbsp;waszych intencjach.</p>
