        <p>1. W&nbsp;dzisiejszą niedzielę obchodzimy Święto Ofiarowania
            Pańskiego. Jest to&nbsp;dzień modlitw w&nbsp;intencji osób życia
            konsekrowanego. Błogosławieństwo świec na&nbsp;każdej
            Mszy&nbsp;św.</p>
        <p>2. Dzisiaj o&nbsp;godz.&nbsp;15.30 będzie wystawienie
            Najświętszego Sakramentu i&nbsp;nabożeństwo różańcowe.
            Po&nbsp;Mszy&nbsp;św. o&nbsp;godz.&nbsp;16.00 zmiana tajemnic
            różańcowych.</p>
        <p>3. Jutro na&nbsp;godz.&nbsp;9.00 zapraszamy chętnych
            do&nbsp;pomocy przy rozbieraniu dekoracji bożonarodzeniowej.</p>
        <p>4. W&nbsp;tym tygodniu przypada pierwszy czwartek i&nbsp;piątek
            miesiąca. Spowiedź w&nbsp;pierwszy piątek
            o&nbsp;godz.&nbsp;17.30. Odwiedzimy chorych z&nbsp;komunia
            świętą.</p>
        <p>5. W&nbsp;przyszłą niedzielę zbiórka do&nbsp;puszek
            na&nbsp;Fundusz Pomocy Chorym Kapłanom Diecezji
            Sandomierskiej.</p>
        <p>6. W&nbsp;minionym tygodniu odwołał Pan Bóg do&nbsp;wieczności
            z&nbsp;naszej parafii Umberto Romanelli, Mariusza Lipczyńskiego
            i&nbsp;Zofię Guściora. Polećmy ich Miłosierdziu Bożemu
            odmawiając: <em>Wieczny odpoczynek...</em></p>
        <p>7. <em>Bóg zapłać</em> za&nbsp;sprzątanie kościoła, złożoną
            ofiarę, dary stołu i&nbsp;wszelką życzliwość.<br />
            <strong>Do&nbsp;sprzątania prosimy w&nbsp;sobotę na&nbsp;godzinę
                7.00</strong> Rodziny z&nbsp;bloku Maciejowskiego 7, numery
            od&nbsp;21 do&nbsp;30 włącznie. <em>Bóg zapłać</em> panu
            Tadeuszowi i&nbsp;Ryszardowi za&nbsp;pracę na&nbsp;rzecz
            parafii.</p>
        <p>8. Na&nbsp;stoliku pod chórem są&nbsp;czasopisma Niedziela
            i&nbsp;Gość Niedzielny. Zachęcamy do&nbsp;nabywania
            i&nbsp;czytania. Cena 6&nbsp;PLN.</p>
        <p>9. Trwają zapisy na&nbsp;pielgrzymkę do&nbsp;Medjugorie oraz
            innych miejsc w&nbsp;Bośni i&nbsp;Hercegowinie, Chorwacji,
            Węgrzech. Bliższe informacje na&nbsp;plakacie w&nbsp;gablocie
            przy wejściu do&nbsp;kościoła oraz w&nbsp;zakrystii.</p>
        <p>10. Wszystkim, którzy w&nbsp;tym tygodniu obchodzą swoje
            imieniny, urodziny, jakiekolwiek rocznice, gościom, uczestnikom
            liturgii, jak również ludziom starszym, schorowanym, którzy już
            osobiście nie mogą uczestniczyć z&nbsp;nami w&nbsp;kościele
            we&nbsp;Mszy&nbsp;św., składamy najlepsze życzenia
            i&nbsp;zapewniamy o&nbsp;naszej modlitwie w&nbsp;waszych
            intencjach.</p>
