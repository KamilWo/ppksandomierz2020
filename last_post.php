        <p>1. <strong>Ponieważ bardzo zwiększyła się liczba zakażeń
        koronawirusem, zamówiłem 4&nbsp;przenośne urządzenia
        do&nbsp;dezynfekcji rąk, które będą stały w&nbsp;kościele przy
        wszystkich drzwiach wejściowych. Dostawca miał przywieźć
        w&nbsp;piątek, ufam, że&nbsp;jutro już będą dostarczone.</strong></p>
        <p>2. W&nbsp;dni powszednie Msza Święta jest odprawiana o&nbsp;6.30
        i&nbsp;18.00. Kto zamawiał intencje do&nbsp;odprawienia, niech
        sprawdza w&nbsp;gablocie na&nbsp;wywieszce. Sposobność
        do&nbsp;spowiedzi przed Mszą Św. zarówno rano, jak
        i&nbsp;wieczorem.</p>
        <p>3. Kościół jest otwarty codziennie przez cały dzień. Zachęcamy
        do&nbsp;modlitwy przed Najświętszym Sakramentem o&nbsp;ustanie
        epidemii i&nbsp;potrzebne łaski dla ducha, i&nbsp;dla ciała.</p>
        <p>4. Od&nbsp;1&nbsp;października odprawiamy nabożeństwo różańcowe
        o&nbsp;godzinie 17.30 w&nbsp;dni powszednie, w&nbsp;niedzielę
        o&nbsp;15.30 (po&nbsp;nabożeństwie Msza Święta).</p>
        <p>5. Dzisiaj o&nbsp;12.30 poświęcenie różańca i&nbsp;Rodzice wręczą
        je&nbsp;swoim dzieciom w&nbsp;ramach przygotowania do&nbsp;I-szej
        Komunii Świętej, która będzie w&nbsp;czwartą niedzielę maja
        w&nbsp;przyszłym roku (23&nbsp;maj). Jest to&nbsp;już decyzja nowego
        księdza Proboszcza.</p>
        <p>6. W&nbsp;tym tygodniu odeszli do&nbsp;wieczności: Józef Ozdoba
        i&nbsp;Jacek Strugała. <em>Wieczny odpoczynek racz im&nbsp;dać
        Panie...</em></p>
        <p>7. <em>Bóg zapłać</em> za&nbsp;sprzątanie kościoła. Msza Święta
        w&nbsp;intencjach sprzątających będzie odprawiona we&nbsp;wtorek
        o&nbsp;godzinie 6.30. <em>Bóg zapłać</em> za&nbsp;dary stołu
        i&nbsp;wszelkie dobro. Wam <strong>Kochani</strong> bardzo dziękuję
        za&nbsp;ofiary, które składacie na&nbsp;tacę, przesyłacie
        na&nbsp;konto bankowe lub przekazujecie w&nbsp;zakrystii.</p>
        <p>8. <strong>Do&nbsp;sprzątania prosimy w&nbsp;sobotę
        na&nbsp;godzinę 7.00</strong> Rodziny z&nbsp;bloku Mickiewicza 43,
        V&nbsp;i&nbsp;VI&nbsp;klatka, numery 36 do&nbsp;60.</p>
        <p>9. Dzisiaj zbiórka do&nbsp;puszek, tzw. świętopietrze. Zbiórka
        przeniesiona przez Władze Kościelne z&nbsp;29&nbsp;czerwca.
        W&nbsp;przyszłą niedzielę zbiórka na&nbsp;wsparcie Dzieła Nowego
        Tysiąclecia – stypendia dla zdolnej a&nbsp;ubogiej młodzieży.</p>
        <p>10. Na&nbsp;stoliku pod chórem jest najnowsza prasa katolicka.
        Zachęcamy do&nbsp;nabywania i&nbsp;czytania prasy katolickiej.</p>
        <p>11. Wszystkim, którzy w&nbsp;tym tygodniu obchodzą swoje imieniny,
        urodziny, jakiekolwiek rocznice, gościom, uczestnikom liturgii,
        składamy najlepsze życzenia i&nbsp;zapewniamy o&nbsp;naszej modlitwie
        w&nbsp;waszych intencjach.</p>

        <h3>Kochani Parafianie i&nbsp;Przyjaciele, którzy byliście razem
        ze&nbsp;mną przez 29&nbsp;lat i&nbsp;3&nbsp;tygodnie. Bardzo dziękuję
        za&nbsp;współpracę, modlitwę i&nbsp;wszelkie dobro, które udało się
        zrealizować w&nbsp;tej Wspólnocie. Dziękuję Kapłanom –
        współpracownikom, Panu Mirkowi – organiście, Pani Monice, która
        po&nbsp;rezygnacji Pana Maćka, z&nbsp;posługi zakrystiana (bardzo
        dziękuję za&nbsp;wspólne lata pracy) pomagała mi&nbsp;przez cały
        czas. Dziękuję Panom Tadeuszowi i&nbsp;Ryszardowi za&nbsp;wszelkie
        prace wykonane dla Parafii i&nbsp;dla mnie. Dziękuję wszystkim
        za&nbsp;wszystko dobro. Co&nbsp;było złego z&nbsp;mej strony
        przepraszam. Zamieszkam w&nbsp;Domu Księży Emerytów, na&nbsp;ulicy
        Staromiejskiej. Pozostańmy ze&nbsp;sobą przez dar modlitwy.</h3>
