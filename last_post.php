        w&nbsp;kościele. Pamiętajmy zatem, że&nbsp;Pan Bóg
        w&nbsp;3&nbsp;przykazaniu mówi nam: "Pamiętaj, abyś dzień święty
        święcił". Zapraszamy do&nbsp;udziału we&nbsp;Mszy Świętej
