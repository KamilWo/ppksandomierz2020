    <div class="card-header">
        <h3>12.&nbsp;kwietnia 2020&nbsp;r.</h3>
        <h3>NIEDZIELA Zmartwychwstania Pańskiego - Rok A</h3>
    </div>
    <div class="col">
        <p>1. <strong>Kochani Moi Parafianie i&nbsp;Goście. Do&nbsp;życzeń
        otrzymywanych od&nbsp;Rodziny, Przyjaciół i&nbsp;Znajomych chcemy
        dołączyć życzenia od&nbsp;nas kapłanów. Życzymy Wam z&nbsp;całego
        serca umocnienia wiary w&nbsp;Zmartwychwstanie Jezusa, wzrastania
        w&nbsp;miłości ku&nbsp;Bogu, który nas kocha, doskonalenia
        w&nbsp;sobie miłości do&nbsp;bliźnich, którzy nas otaczają oraz
        ożywiania nadziei, że&nbsp;lęk i&nbsp;przygnębienie, które
        przeżywamy z&nbsp;racji trwającej na&nbsp;całym globie epidemii
        koronawirusa minie i&nbsp;znowu będziemy mogli cieszyć się pełną
        swobodą obywatelską w&nbsp;naszej Ojczyźnie. Zmartwychwstały Jezus
        pokonał przecież szatana, grzech i&nbsp;wszelkie lęki. Wyszedł
        z&nbsp;grobu i&nbsp;mówi wciąż do&nbsp;nas: nie lękajcie się.
        Zdrowych, radosnych i&nbsp;błogosławionych Świąt Zmartwychwstania
        Pańskiego.</strong></p>
        <p>2. Dzisiaj i&nbsp;jutro porządek Mszy Świętych w&nbsp;naszym
        kościele jak w&nbsp;każdą niedzielę, także o&nbsp;godzinie 20.30.
        <strong>Podczas nabożeństw w&nbsp;kościele może przebywać tylko
        5&nbsp;osób.</strong></p>
        <p>3. Przypominamy, że&nbsp;Ks.&nbsp;bp udzielił dyspensy
        od&nbsp;udziału we&nbsp;Mszy Świętej w&nbsp;niedziele
        do&nbsp;odwołania. Zachęca nas jednak do&nbsp;udziału we&nbsp;Mszy
        Świętej korzystając z&nbsp;mediów zarówno ogólnopolskich, jak
        i&nbsp;lokalnych.</p>
        <p>4. Aby umożliwić wiernym udział we&nbsp;Mszy Świętej
        (ze&nbsp;względu na&nbsp;ograniczenia) w&nbsp;dni powszednie Msza
        Święta będzie odprawiona o&nbsp;godzinie <strong>6.30, 7.00, 17.30
        i&nbsp;18.00</strong>. Kościoły są&nbsp;otwarte przez cały dzień,
        zachęcamy do&nbsp;modlitwy przed Najświętszym Sakramentem
        o&nbsp;ustanie epidemii.</p>
        <p>5. Katechizacja przygotowująca do&nbsp;Sakramentu małżeństwa dla
        tych, którzy planują brać ślub do&nbsp;września jest prowadzona przez
        Internet. Katechezy we&nbsp;wtorek i&nbsp;piątek o&nbsp;godzinie
        20.00. Informacja szczegółowa dla zainteresowanych jest
        w&nbsp;gablocie.</p>
        <p>6. <em>Bóg zapłać</em> za&nbsp;sprzątanie kościoła i&nbsp;złożoną
        ofiarę Panom Michałowi i&nbsp;Sławomirowi, za&nbsp;przygotowanie
        Ciemnicy i&nbsp;Grobu Pańskiego Pani Monice oraz Panom: Ryszardowi,
        Tadeuszowi i&nbsp;Mateuszowi. <em>Bóg zapłać</em> za&nbsp;kwiaty
        ofiarowane do&nbsp;kościoła Rodzinie Krzysztofa i&nbsp;Beaty.</p>
        <p>7. <strong>Do&nbsp;sprzątania prosimy w&nbsp;sobotę
        na&nbsp;godzinę 7.00</strong> Rodziny z&nbsp;bloku Maciejowskiego 6
        od&nbsp;41 do&nbsp;45 włącznie. (wyznaczamy mniej, bo&nbsp;może być
        tylko 5&nbsp;osób w&nbsp;grupie).</p>
        <p>8. Na&nbsp;stoliku pod chórem jest najnowsza prasa katolicka.
        Zachęcamy do&nbsp;nabywania i&nbsp;czytania prasy katolickiej.</p>
        <p>9. Trwają zapisy na pielgrzymkę do&nbsp;Medjugorie oraz innych
        miejsc w&nbsp;Bośni i&nbsp;Hercegowinie, Chorwacji, Węgrzech. Bliższe
        informacje na&nbsp;plakacie w&nbsp;gablocie przy wejściu
        do&nbsp;kościoła oraz w&nbsp;zakrystii.</p>
        <p>10. Wszystkim, którzy w&nbsp;tym tygodniu obchodzą swoje imieniny,
        urodziny, jakiekolwiek rocznice, gościom, uczestnikom liturgii,
        składamy najlepsze życzenia i&nbsp;zapewniamy o&nbsp;naszej modlitwie
        w&nbsp;waszych intencjach.</p>
