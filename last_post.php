        <h3>Wakacje minęły. Msza Święta na&nbsp;rozpoczęcie roku
        szkolnego 2020/21 będzie we&nbsp;wtorek o&nbsp;godzinie 8.00.
        Zapraszamy do&nbsp;udziału dzieci, młodzież, Rodziców,
        Nauczycieli.</h3>

        <p>1. <strong>Jak wiemy, od&nbsp;30&nbsp;maja nie ma&nbsp;ograniczeń
        co&nbsp;do&nbsp;liczby uczestników we&nbsp;Mszy Świętej
        w&nbsp;kościele. Pamiętajmy zatem, że&nbsp;Pan Bóg
        w&nbsp;3&nbsp;przykazaniu mówi nam: "Pamiętaj, abyś dzień święty
        święcił". Zapraszamy do&nbsp;udziału we&nbsp;Mszy Świętej
        w&nbsp;każdą niedzielę i&nbsp;święto nakazane wszystkich parafian:
        dzieci, młodzież i&nbsp;dorosłych.</strong></p>
        <p>2. W&nbsp;dni powszednie Msza Święta jest odprawiana o&nbsp;6.30
        i&nbsp;18.00. Kto zamawiał intencje do&nbsp;odprawienia, niech
        sprawdza w&nbsp;gablocie na&nbsp;wywieszce. Sposobność
        do&nbsp;spowiedzi przed Mszą&nbsp;Św. zarówno rano, jak
        i&nbsp;wieczorem.</p>
        <p>3. Kościół jest otwarty przez cały dzień, zachęcamy
        do&nbsp;modlitwy przed Najświętszym Sakramentem o&nbsp;ustanie
        epidemii.</p>
        <p>4. W&nbsp;tym tygodniu jest I&nbsp;czwartek, piątek i&nbsp;sobota
        miesiąca września. W&nbsp;I&nbsp;piątek okazja do&nbsp;spowiedzi
        od&nbsp;godziny 17.00.</p>
        <p>5. 7&nbsp;września jest piesza pielgrzymka do&nbsp;Sulisławic.</p>
        <p>6. W&nbsp;minionym tygodniu odwołał Pan Bóg do&nbsp;wieczności
        z&nbsp;naszej wspólnoty Mieczysława Kunkiewicza, którego pogrzeb
        będzie we&nbsp;wtorek o&nbsp;godzinie 14.00. <em>Wieczny odpoczynek
        racz mu&nbsp;dać Panie...</em></p>
        <p>7. <em>Bóg zapłać</em> za&nbsp;rodzinne sprzątanie kościoła oraz
        zamówioną Mszę Świętą w&nbsp;intencjach wczoraj sprzątających rodzin,
        która będzie odprawiona 7&nbsp;września o&nbsp;godzinie 18.00.
        <em>Bóg zapłać</em> Panu Piotrowi za&nbsp;zakup 2&nbsp;drzew
        i&nbsp;krzewów i&nbsp;posadzenie na&nbsp;placu wraz
        z&nbsp;P.&nbsp;Halinką, jako rekompensata za&nbsp;wycięcie drzewa,
        które wrastało i&nbsp;zagrażało rozwaleniem WC&nbsp;wybudowanemu
        na&nbsp;placu kościelnym. Wam <strong>Kochani</strong> bardzo
        dziękuję za&nbsp;ofiary, które składacie na&nbsp;tacę, przesyłacie
        na&nbsp;konto bankowe lub przekazujecie w&nbsp;zakrystii.</p>
        <p>8. <strong>Do&nbsp;sprzątania prosimy w&nbsp;sobotę
        na&nbsp;godzinę 7.00</strong> Rodziny z&nbsp;bloku Maciejowskiego 2
        od&nbsp;31 do&nbsp;40 włącznie.</p>
        <p>9. Na&nbsp;stoliku pod chórem jest najnowsza prasa katolicka.
        Zachęcamy do&nbsp;nabywania i&nbsp;czytania prasy katolickiej.</p>
        <p>10. Wszystkim, którzy w&nbsp;tym tygodniu obchodzą swoje imieniny,
        urodziny, jakiekolwiek rocznice, gościom, uczestnikom liturgii,
        składamy najlepsze życzenia i&nbsp;zapewniamy o&nbsp;naszej modlitwie
        w&nbsp;waszych intencjach.</p>
