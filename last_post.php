        <h3>Informuję Parafian, że&nbsp;pasterz Diecezji mianował Proboszczem
        Parafii Podwyższenia Krzyża Świętego w&nbsp;Sandomierzu
        ks.&nbsp;kanonika Stanisława Chmielewskiego. Informację z&nbsp;Kurii
        otrzymałem telefonicznie we&nbsp;czwartek, w&nbsp;piątek przyszło
        pismo pocztą, protokół zdawczo-odbiorczy w&nbsp;obecności księdza
        Dziekana dekanatu sandomierskiego i&nbsp;dwóch świadków
        z&nbsp;Parafii (Pana Ryszarda i&nbsp;Pana Tadeusza) podpiszemy
        we&nbsp;wtorek 6&nbsp;października. Liturgicznie Ksiądz Stanisław
        Chmielewski obejmie urząd proboszcza parafii pw.&nbsp;Podwyższenia
        Krzyża Świętego w&nbsp;Sandomierzu, w&nbsp;obecności księdza
        dziekana, w&nbsp;niedzielę 11&nbsp;października o&nbsp;godzinie
        11.15. Informując o&nbsp;tym wydarzeniu zapraszamy na&nbsp;mszę
        Świętą i&nbsp;prosimy o&nbsp;modlitwę, życząc Nowemu księdzu
        Proboszczowi zdrowia i&nbsp;obfitych Łask Bożych do&nbsp;owocnego
        sprawowania posługi Proboszcza.</h3>

        <img src="static/download/20201004_074921.webp"
